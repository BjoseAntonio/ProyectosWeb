//Diferencia entre  metodos y función 
//Función 
// Una función es un bloque de código que realiza una tarea específica.Puede recibir cero o más parámetros y puede devolver un valor.En la programación orientada a objetos, una función que está fuera de cualquier clase se denomina "función" o "método global".

//Metodos
// Los métodos son funciones que están asociadas a un objeto específico.
// Se definen dentro del cuerpo de un objeto y se asignan como valores de propiedades de ese objeto.

const numero1  = 30;
const numero2 = '334';

console.log(parseInt(numero2)); //parseInt() es una funcion 
console.log(numero2.toString()); // .toString() es un método

